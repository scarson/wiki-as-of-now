// ABOUTME: Asserts both wrangler configs carry the Phase-7 deploy-prep shape.
// ABOUTME: AI binding on both workers, per-env blocks, SSRF flag, no nodejs_compat on research, no cron yet.
import { describe, it, expect } from "vitest";
import { readFileSync } from "node:fs";
import { resolve } from "node:path";

const root = resolve(__dirname, "../..");

// Structural shape of the slices of a wrangler config this suite asserts on. Optional
// throughout because the same reader parses both worker configs and every env block, and
// the assertions are what pin which fields must actually be present.
interface QueueProducer {
  binding: string;
  queue: string;
}
interface QueueConsumer {
  queue: string;
  max_batch_size?: number;
  max_retries?: number;
  dead_letter_queue?: string;
}
interface QueuesBlock {
  producers?: QueueProducer[];
  consumers?: QueueConsumer[];
}
interface D1Binding {
  binding: string;
  database_name?: string;
  database_id?: string;
}
interface ServiceBinding {
  binding: string;
  service: string;
}
interface RouteBinding {
  pattern: string;
  custom_domain?: boolean;
}
interface EnvBlock {
  name?: string;
  services?: ServiceBinding[];
  d1_databases?: D1Binding[];
  queues?: QueuesBlock;
  triggers?: { crons?: string[] };
  routes?: RouteBinding[];
  vars?: Record<string, string>;
  ai?: { binding: string; remote?: boolean };
  images?: { binding: string };
}
interface WranglerConfig {
  ai?: { binding: string; remote?: boolean };
  compatibility_flags?: string[];
  triggers?: { crons?: string[] };
  vars?: Record<string, string>;
  d1_databases?: D1Binding[];
  env?: { dev?: EnvBlock; production?: EnvBlock };
}

// Minimal JSONC reader: drop block + line comments, then JSON.parse.
// Worker configs are author-controlled (not untrusted), so a simple strip is safe here.
// The line-comment strip preserves a leading non-comment char so it never eats a "://" in a URL.
function readJsonc(relPath: string): WranglerConfig {
  const raw = readFileSync(resolve(root, relPath), "utf8");
  const noBlock = raw.replace(/\/\*[\s\S]*?\*\//g, "");
  const noLine = noBlock.replace(/(^|[^:"])\/\/.*$/gm, "$1");
  return JSON.parse(noLine) as WranglerConfig;
}

describe("wrangler config — AI binding (Task 7.1)", () => {
  // The shipped bindings carry `remote: true` (Phase 1 deviation D4 — AI bindings have no
  // local emulation, so remote:true silences the Miniflare stderr warning). Assert the real
  // shipped shape, not an idealized one (testing-pitfalls §6: test the config that ships).
  it("root app worker declares the AI binding", () => {
    const cfg = readJsonc("wrangler.jsonc");
    expect(cfg.ai?.binding).toBe("AI");
  });

  it("research worker declares the AI binding", () => {
    const cfg = readJsonc("workers/research/wrangler.jsonc");
    expect(cfg.ai?.binding).toBe("AI");
  });

  it("cloudflare-env.d.ts exposes AI (app worker, regenerated by cf-typegen)", () => {
    const dts = readFileSync(resolve(root, "cloudflare-env.d.ts"), "utf8");
    expect(dts).toMatch(/\bAI\s*:\s*Ai\b/);
  });

  it("research worker env type declares AI by hand (cf-typegen does not see it)", () => {
    const src = readFileSync(resolve(root, "workers/research/index.ts"), "utf8");
    expect(src).toMatch(/interface ResearchWorkerEnv[\s\S]*?\bAI\s*:\s*Ai\b/);
  });

  it("every named env re-declares the bindings wrangler does NOT inherit (ai on both workers; images on the app)", () => {
    // Bindings are non-inheritable: a named-env deploy silently drops any binding declared
    // only at the top level (wrangler warns '"ai" exists at the top level, but not on "env.dev"').
    // A dev/production research worker without env.AI crashes the consumer at the first message.
    const app = readJsonc("wrangler.jsonc");
    const research = readJsonc("workers/research/wrangler.jsonc");
    for (const which of ["dev", "production"] as const) {
      expect(app.env?.[which]?.ai?.binding).toBe("AI");
      expect(app.env?.[which]?.images?.binding).toBe("IMAGES");
      expect(research.env?.[which]?.ai?.binding).toBe("AI");
    }
  });
});

describe("wrangler config — per-env blocks (Task 7.2)", () => {
  // Narrow an optional env block to a present one. Throwing here IS the presence assertion:
  // a missing env block fails the test with a clear message instead of a downcast `undefined`.
  function envOf(cfg: WranglerConfig, which: "dev" | "production"): EnvBlock {
    const block = cfg.env?.[which];
    if (!block) throw new Error(`expected env.${which} block to be present`);
    return block;
  }

  it("app worker has dev + production env blocks with distinct worker names", () => {
    const cfg = readJsonc("wrangler.jsonc");
    expect(envOf(cfg, "dev").name).toBe("wiki-as-of-now-dev");
    expect(envOf(cfg, "production").name).toBe("wiki-as-of-now");
  });

  it("app worker per-env self-reference service matches the per-env worker name", () => {
    const cfg = readJsonc("wrangler.jsonc");
    const devSvc = envOf(cfg, "dev").services?.find((s) => s.binding === "WORKER_SELF_REFERENCE");
    const prodSvc = envOf(cfg, "production").services?.find((s) => s.binding === "WORKER_SELF_REFERENCE");
    expect(devSvc?.service).toBe("wiki-as-of-now-dev");
    expect(prodSvc?.service).toBe("wiki-as-of-now");
  });

  it("app worker per-env queue producer points at the per-env queue", () => {
    const cfg = readJsonc("wrangler.jsonc");
    expect(envOf(cfg, "dev").queues?.producers?.[0].queue).toBe("research-dev");
    expect(envOf(cfg, "production").queues?.producers?.[0].queue).toBe("research");
  });

  it("research worker dev queues are -dev suffixed; prod queues are bare", () => {
    const cfg = readJsonc("workers/research/wrangler.jsonc");
    const dev = envOf(cfg, "dev").queues;
    const prod = envOf(cfg, "production").queues;
    expect(dev?.producers?.[0].queue).toBe("research-dev");
    expect(dev?.consumers?.[0].queue).toBe("research-dev");
    expect(dev?.consumers?.[0].dead_letter_queue).toBe("research-dlq-dev");
    expect(prod?.producers?.[0].queue).toBe("research");
    expect(prod?.consumers?.[0].queue).toBe("research");
    expect(prod?.consumers?.[0].dead_letter_queue).toBe("research-dlq");
  });

  it("research worker keeps max_batch_size 1 in both envs (G14 sequential drain)", () => {
    const cfg = readJsonc("workers/research/wrangler.jsonc");
    expect(envOf(cfg, "dev").queues?.consumers?.[0].max_batch_size).toBe(1);
    expect(envOf(cfg, "production").queues?.consumers?.[0].max_batch_size).toBe(1);
  });

  it("research worker per-env names are distinct", () => {
    const cfg = readJsonc("workers/research/wrangler.jsonc");
    expect(envOf(cfg, "dev").name).toBe("wiki-as-of-now-research-dev");
    expect(envOf(cfg, "production").name).toBe("wiki-as-of-now-research");
  });

  it("both workers point at the same per-env D1 database_name (shared DB per env, CC-10)", () => {
    const app = readJsonc("wrangler.jsonc");
    const research = readJsonc("workers/research/wrangler.jsonc");
    expect(envOf(app, "dev").d1_databases?.[0].database_name).toBe("wiki-as-of-now-dev");
    expect(envOf(research, "dev").d1_databases?.[0].database_name).toBe("wiki-as-of-now-dev");
    expect(envOf(app, "production").d1_databases?.[0].database_name).toBe("wiki-as-of-now");
    expect(envOf(research, "production").d1_databases?.[0].database_name).toBe("wiki-as-of-now");
  });

  it("per-env D1 ids are real, shared across workers (CC-10), and distinct across envs", () => {
    const app = readJsonc("wrangler.jsonc");
    const research = readJsonc("workers/research/wrangler.jsonc");
    const uuid = /^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}$/;
    const devId = envOf(app, "dev").d1_databases?.[0].database_id;
    const prodId = envOf(app, "production").d1_databases?.[0].database_id;
    expect(devId).toMatch(uuid);
    expect(prodId).toMatch(uuid);
    expect(envOf(research, "dev").d1_databases?.[0].database_id).toBe(devId);
    expect(envOf(research, "production").d1_databases?.[0].database_id).toBe(prodId);
    expect(devId).not.toBe(prodId);
    // The top-level (default/Miniflare) ids stay the all-zeros placeholder — CI dry-run target.
    expect(app.d1_databases?.[0].database_id).toBe("00000000-0000-0000-0000-000000000000");
  });

  it("the DLQ is never declared as a producer/consumer binding (inferred from dead_letter_queue only)", () => {
    const cfg = readJsonc("workers/research/wrangler.jsonc");
    const dev = envOf(cfg, "dev").queues;
    const prod = envOf(cfg, "production").queues;
    const allQueueRefs = [
      ...(dev?.producers ?? []).map((p) => p.queue),
      ...(dev?.consumers ?? []).map((c) => c.queue),
      ...(prod?.producers ?? []).map((p) => p.queue),
      ...(prod?.consumers ?? []).map((c) => c.queue),
    ];
    expect(allQueueRefs).not.toContain("research-dlq");
    expect(allQueueRefs).not.toContain("research-dlq-dev");
  });

  it("both workers keep global_fetch_strictly_public and research keeps no nodejs_compat", () => {
    const app = readJsonc("wrangler.jsonc");
    const research = readJsonc("workers/research/wrangler.jsonc");
    expect(app.compatibility_flags).toContain("global_fetch_strictly_public");
    expect(research.compatibility_flags).toContain("global_fetch_strictly_public");
    expect(research.compatibility_flags).not.toContain("nodejs_compat");
  });

  it("no cron triggers are declared in either config yet (cron is the last go-live step)", () => {
    const app = readJsonc("wrangler.jsonc");
    const research = readJsonc("workers/research/wrangler.jsonc");
    expect(app.triggers?.crons ?? []).toEqual([]);
    expect(research.triggers?.crons ?? []).toEqual([]);
    expect(app.env?.dev?.triggers?.crons ?? []).toEqual([]);
    expect(app.env?.production?.triggers?.crons ?? []).toEqual([]);
    expect(research.env?.dev?.triggers?.crons ?? []).toEqual([]);
    expect(research.env?.production?.triggers?.crons ?? []).toEqual([]);
  });

  it("production app worker serves the custom domain with APP_ORIGIN matching (go-live)", () => {
    const app = readJsonc("wrangler.jsonc");
    const prod = envOf(app, "production");
    expect(prod.routes).toEqual([{ pattern: "wikinow.scarson.io", custom_domain: true }]);
    expect(prod.vars?.APP_ORIGIN).toBe("https://wikinow.scarson.io");
  });

  it("dev app worker stays on workers.dev with no custom routes or APP_ORIGIN", () => {
    const app = readJsonc("wrangler.jsonc");
    expect(envOf(app, "dev").routes).toBeUndefined();
    expect(envOf(app, "dev").vars?.APP_ORIGIN).toBeUndefined();
  });

  it("RESEARCH_PROVIDER never appears in the research worker top-level config (CC-7 / workerd pool)", () => {
    // The workerd vitest pool loads the research config TOP-LEVEL (test/workers/test-env.ts);
    // a top-level provider var would flip the pool off the stub default the CC-7 tests hardwire.
    const research = readJsonc("workers/research/wrangler.jsonc");
    expect(research.vars?.RESEARCH_PROVIDER).toBeUndefined();
  });

  it("dev research worker runs the real workers-ai provider; production stays on the stub until its go-live flip", () => {
    const research = readJsonc("workers/research/wrangler.jsonc");
    expect(envOf(research, "dev").vars?.RESEARCH_PROVIDER).toBe("workers-ai");
    expect(envOf(research, "production").vars?.RESEARCH_PROVIDER).toBeUndefined();
  });
});

